  /// Search foods by name
  static List<FoodItem> search(String query) {
    if (query.isEmpty) return foods;
    final lowerQuery = query.toLowerCase();
    return foods.where((food) {
      return food.name.toLowerCase().contains(lowerQuery) ||
          food.category.displayName.toLowerCase().contains(lowerQuery) ||
          (food.brand?.toLowerCase().contains(lowerQuery) ?? false);
    }).toList();
  }

  /// Get foods by category
  static List<FoodItem> byCategory(FoodCategory category) {
    return foods.where((food) => food.category == category).toList();
  }

  /// Get food by ID
  static FoodItem? getById(String id) {
    try {
      return foods.firstWhere((food) => food.id == id);
    } catch (_) {
      return null;
    }
  }

  /// Get recent/popular foods (for quick access)
  static List<FoodItem> get popularFoods {
    return [
      getById('chicken_breast')!,
      getById('eggs')!,
      getById('brown_rice')!,
      getById('banana')!,
      getById('greek_yogurt')!,
      getById('oatmeal')!,
      getById('avocado')!,
      getById('salmon')!,
    ];
  }

  /// Get high protein foods
  static List<FoodItem> get highProteinFoods {
    return foods.where((food) => food.nutrition.protein >= 15).toList()
      ..sort((a, b) => b.nutrition.protein.compareTo(a.nutrition.protein));
  }

  /// Get low calorie foods
  static List<FoodItem> get lowCalorieFoods {
    return foods.where((food) => food.nutrition.calories <= 100).toList()
      ..sort((a, b) => a.nutrition.calories.compareTo(b.nutrition.calories));
  }
