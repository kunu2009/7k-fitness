  /// Get all exercises
  static List<ExerciseDefinition> get all => _exercises;

  /// Get exercises by muscle group
  static List<ExerciseDefinition> getByMuscleGroup(MuscleGroup muscleGroup) {
    return _exercises
        .where(
          (e) =>
              e.primaryMuscles.contains(muscleGroup) ||
              e.secondaryMuscles.contains(muscleGroup),
        )
        .toList();
  }

  /// Get exercises by difficulty
  static List<ExerciseDefinition> getByDifficulty(Difficulty difficulty) {
    return _exercises.where((e) => e.difficulty == difficulty).toList();
  }

  /// Get exercises by equipment
  static List<ExerciseDefinition> getByEquipment(Equipment equipment) {
    return _exercises.where((e) => e.equipment.contains(equipment)).toList();
  }

  /// Get exercises by category
  static List<ExerciseDefinition> getByCategory(ExerciseCategory category) {
    return _exercises.where((e) => e.category == category).toList();
  }

  /// Get exercises that require no equipment
  static List<ExerciseDefinition> getBodyweightExercises() {
    return _exercises
        .where(
          (e) =>
              e.equipment.isEmpty ||
              (e.equipment.length == 1 && e.equipment.contains(Equipment.none)),
        )
        .toList();
  }

  /// Search exercises by name
  static List<ExerciseDefinition> search(String query) {
    final lowercaseQuery = query.toLowerCase();
    return _exercises
        .where(
          (e) =>
              e.name.toLowerCase().contains(lowercaseQuery) ||
              e.description.toLowerCase().contains(lowercaseQuery) ||
              e.muscleText.toLowerCase().contains(lowercaseQuery),
        )
        .toList();
  }

  /// Get exercise by ID
  static ExerciseDefinition? getById(String id) {
    try {
      return _exercises.firstWhere((e) => e.id == id);
    } catch (e) {
      return null;
    }
  }

  /// Get compound exercises
  static List<ExerciseDefinition> getCompoundExercises() {
    return _exercises.where((e) => e.isCompound).toList();
  }

  /// Get isolation exercises
  static List<ExerciseDefinition> getIsolationExercises() {
    return _exercises.where((e) => !e.isCompound).toList();
  }

  /// Get exercises count
  static int get count => _exercises.length;

  /// Get all exercises (alias for UI compatibility)
  static List<ExerciseDefinition> get allExercises => _exercises;

  /// Get all exercises
  static List<ExerciseDefinition> getAll() => _exercises;
